class Solution
{
    public:
    Node* insertionSort(struct Node* head)
    {
        for(Node* i=head;i->next!=NULL;i=i->next){
            Node *min=i;
            for(Node *j=i->next;j!=NULL;j=j->next){
                if(min->data>j->data) min=j;
            }
            if(min!=i){
                int k=i->data;
                i->data=min->data;
                min->data=k;
            }
        }
        return head;
    }
    
};

